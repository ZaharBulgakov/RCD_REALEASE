/**
 * Simple character-based similarity between two strings.
 * Returns a value between 0 and 1.
 */
export function getStringSimilarity(s1: string, s2: string): number {
  const str1 = s1.toLowerCase().replace(/[^a-zа-я0-9]/g, "");
  const str2 = s2.toLowerCase().replace(/[^a-zа-я0-9]/g, "");
  
  if (str1 === str2) return 1.0;
  if (str1.length === 0 || str2.length === 0) return 0;
  
  // Use a simple bigram-like approach for character coincidence
  const pairs1 = getPairs(str1);
  const pairs2 = getPairs(str2);
  
  const union = pairs1.length + pairs2.length;
  let intersection = 0;
  
  const p2Set = new Map();
  pairs2.forEach(p => p2Set.set(p, (p2Set.get(p) || 0) + 1));
  
  pairs1.forEach(p => {
    if (p2Set.has(p) && p2Set.get(p) > 0) {
      intersection++;
      p2Set.set(p, p2Set.get(p) - 1);
    }
  });
  
  return (2.0 * intersection) / union;
}

function getPairs(s: string): string[] {
  const pairs = [];
  for (let i = 0; i < s.length - 1; i++) {
    pairs.push(s.substring(i, i + 2));
  }
  return pairs;
}

export function findBestOpeningMatch(input: string): [string, string] | null {
  if (!input || input.trim().length < 2) return null;
  
  let bestMatch: [string, string] | null = null;
  let maxSimilarity = 0.3; // Threshold
  
  for (const [name, pgn] of OPENINGS_DATABASE) {
    const sim = getStringSimilarity(input, name);
    if (sim > maxSimilarity) {
      maxSimilarity = sim;
      bestMatch = [name, pgn];
    }
  }
  
  return bestMatch;
}
